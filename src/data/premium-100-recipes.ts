// Convenience Functions
export const getPremium100Recipes = (): Recipe[] => PREMIUM_100_RECIPES

export const getRecipesByCategory = (category: string): Recipe[] =>
  PREMIUM_100_RECIPES.filter(recipe => recipe.category === category)

export const getRecipesByCuisine = (cuisine: string): Recipe[] =>
  PREMIUM_100_RECIPES.filter(recipe => recipe.cuisine === cuisine)

export const getQuickRecipes = (maxMinutes: number = 30): Recipe[] =>
  PREMIUM_100_RECIPES.filter(recipe => recipe.cookTime <= maxMinutes)

export const getTopRated = (limit: number = 20): Recipe[] =>
  PREMIUM_100_RECIPES
    .filter(recipe => recipe.rating >= 4.3)
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit)

export const searchPremiumRecipes = (query: string): Recipe[] =>
  PREMIUM_100_RECIPES.filter(recipe =>
    recipe.title.toLowerCase().includes(query.toLowerCase()) ||
    recipe.ingredients.some(ingredient =>
      ingredient.toLowerCase().includes(query.toLowerCase())
    ) ||
    recipe.tags.some(tag => tag.toLowerCase().includes(query.toLowerCase()))
  )

